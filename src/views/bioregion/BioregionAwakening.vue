    <div style="text-align: center;"><h3>Artist Statement</h3><br>Sara Siestreem (<span style="font-variant: small-caps;">hanis coos</span>)</div>

    <p class="quote-3"><span style="font-size: 80%;"><br><strong>Disclaimer:</strong> except for noted quotation and paraphrase throughout this document, the content within is solely that of the artist and reflects her perspective.<br><br></span></p>

    <p>Fine art is a manifestation of a culture that is living in plenty (Larry Campbell, <span style="font-variant: small-caps;">swinomish</span>). In the case of historic Indigenous Fine Art, we must be living in harmony and close contact with the land. The practice is integrated into our day-to-day lifeways (Greg Archuleta, <span style="font-variant: small-caps;">grand ronde</span>). For us, the Coos, Lower Umpqua, and Siuslaw people, that way of life and those art forms were disrupted by the occupation of the United States on our land in the 1850’s. Our cultural arts went into hibernation at that time.</p>

    <p>Through my work in institutional reform beginning in 2010, I gained unique access to historic collections of Coos material and contemporary knowledge holders from the surrounding Tribes. I began my study of the technical, cultural, and field aspects of Indigenous weaving in this region with Greg Archuleta (<span style="font-variant: small-caps;">grand ronde</span>) and Greg A. Robinson (<span style="font-variant: small-caps;">chinook</span>). In the field, I learn from the animals, the plants, and the land. I study my ancestors through visiting and photo documenting the baskets they made for us that are housed in regional museum collections.</p>

    <p>From that work I started The Hanis Coos Traditional Weaving Research and Education Project to revive the practice for my people. With the support of my Tribal government and The Evergreen Longhouse, I opened our Tribal field-to-studio weaving program in 2014. In 2016 the Potlatch Fund and the Oregon Community Foundation joined that pool of support and our weaving program continues to grow steadily. Today I have twenty-five weaving students ranging in age from five to eighty-five and one very bright and talented apprentice, Ashley Russell (<span style="font-variant: small-caps;">miluk coos</span>).</p>

    <img src="images/CacheVII.jpg" class="fullwidth">
    <div class="caption"><em>Cache VII</em>, Sara Siestreem, PNCA 2018</div>

    <p>In an effort to share this awakening with the mainstream I exhibit each year’s cache in public spaces. This is an occupation of public land; an overt political act. To gather and possess these plants is an exercise of sovereign rights, a legal provision for Indigenous people of this land mass to continue to practice our cultural and spiritual birthright. Each plant represents a different relationship I initiate and maintain with Federal, State, and private land owners.</p>

    <p>More importantly, every very single section of this cache represents long relationships I have with the places I gather and the plants themselves. From an Indigenous world view, plants, animals, and elements are equals, we are relatives. It is forbidden to take anything without permission, compensation, follow through on promises made, and within the boundaries of sustainability. That means I must establish and gain permission from the land and plant itself. I cannot do that in one day, it takes years. I must visit these places and plants again and again before I am allowed to gather them.</p>

    <p>We practice the continuum of living culture. That means this knowledge is passed down one generation to the next, which includes the conversation we are having with the plants and the land. These plants remember us from our grandfathers and grandmothers and all of our knowledge and technology is collaboration between these entities. I am picking up that conversation that has been going on for thousands of years, since the beginning of human occupation in these places. It is my responsibility to maintain this conversation for the rest of my life and initiate introductions between my people and the land and to maintain those relationships as each new generation is born. The plants you are sharing space with have agreed to be here, to be a part of your education. I told them about you. The ones who gathered them and prepared them for today also agreed to be a part of your education. I told them about you.</p>

    <p>I am sharing this unique and privileged insight to our culture to benefit your education and give you a better understanding of the place you live and the people whose land you are on. To the Indigenous people who pass through here I hope these plants bring you some comfort and good company. I hope they remind you of who you are and how beautiful, powerful, and sophisticated your culture is. I hope that the ancestors that you carry with you can feel my love and gratitude for them.</p>

    <p>I am speaking to you through all of your senses except touch and taste. You can see these plants and the perfection of their design, hear their movement when your body stirs the air in the space, you can smell them as you bring the medicine they are giving you in with your breath. By stimulating your senses, I am situating us in your memory bank. I am informing you that we are living and practicing our culture all around you. By not allowing you physical contact, I am exerting a boundary, showing you how to love us without trespass.</p>

    <img src="images/Ceiling.jpg" class="fullwidth">

    <p>I gathered this cache of weaving materials in the Columbia River Bioregion. Portions of it I gathered in workshop with my Tribal weaving students in our ancestral homeland on the South Coast of Oregon, for some of them it was a first gathering. This is a particularly precious point of joy and I hope you recognize it for the tender and powerful moment that it is. You are witnessing a victory over genocide here archived by children and generations of grandmothers and grandfathers; please let that connection impact your day today.</p>

    <p>Other parts of this cache I gathered on public lands set aside as wildlife preserve and public recreation. This is a political act of sovereign rights. I am educating governmental entities to include us in their sphere of service and understand our rights as they pertain to their jurisdiction. I teach them that the spiritual protocols of gathering require privacy from outsiders. We are not there for their benefit to entertain or utilize in any way. By creating this understanding I am seeding the field for the next generations. I am teaching my students how to navigate these protocols for themselves so they are safe and undisturbed during their work with the land.</p>

    <p>I will take these materials into workshop in 2019. The next time you see these plants they will be baskets woven by Coos, Lower Umpqua, and Siuslaw people. The education that you have gained through visiting with these plants will be embedded into those baskets. They will remember you and this time in your life. Through your witness and education, this will be a cross cultural victory over genocide.</p>

