        <p>The "seventh generation" is a measure that Native Americans often use to describe the stewardship responsibilities incurred by the harvest of animals and plants, or by any other exploitation of the land. In short: Use it in a way that respects the plants and animals and leaves equal benefit for others, as far down as the seventh generation. Seven generations is roughly 150 years&mdash;a long time for settlers whose North American ancestors might not even go back that far, but still, an imaginable period of time for any human being.</p>

        <p>In 1999, the State and Tribal Working Group authored a report entitled <a href="https://www.energy.gov/sites/prod/files/2018/07/f53/STGWG_Closure_for_7th_Gen_Report_%282017%29.pdf" target="_blank" rel="nofollow">Closure for the Seventh Generation</a>, which sets forth localized views of the standards to which the federal Department of Energy should adhere in its environmental cleanup and eventual handover of contaminated nuclear sites. In 2018 Dan Serres, conservation director for Columbia Riverkeeper, drew inspiration from the 2017 version of that report to assess DOE's most recent plans for the Hanford site.</p>

        <p><em>Closure for the Seventh Generation</em> stresses the uncertainty of so-called "institutional controls," such as land-use laws, KEEP OUT signs, or even engineered fences and barriers that may not withstand the test of time. In the Columbia Riverkeeper report, Dan Serres stresses the high value that large tracts of cleared or never-developed land along a free-flowing stretch of the Columbia River hold for Native Americans in particular, and indeed, for everyone. Will guards be funded, will fences remain standing, will future users of the site even notice rusting signs that once read DANGER or KEEP OUT?</p>

        <p>The question is crucially important because DOE now envisages reserving certain portions of the Hanford site for "industrial uses only," a restriction that depends on the maintenance of institutional controls and does not take into account either the desire of Native Americans to inhabit lands that were traditionally theirs, nor the use of riverine landscapes by visitors from nearby cities seeking recreation and another perspective on life. The same question becomes even more relevant in light of recently announced DOE plans to reclassify much of the radioactive debris currently stored in leaking tanks on the Central Plateau as "low-level waste," and then simply pour concrete over it and leave it to the elements. Apparently the seventh generation does not exist for DOE, which nonetheless holds responsibility for materials whose toxicty is known to last for millennia.</p>

        <p>Tribes, citizens and lawmakers in the states of Washington and Oregon, and indeed, throughout Cascadia, have of a very different vision of the natural environment that they would like to enjoy and to leave to their children and grandchildren, down to or even beyond the seventh generation. The damming of the Columbia River and its tributaries, as well as the current reckless exploitation of North American fossil-fuel reserves, can make that vision appear as a cruel and hypocritical lie, no different in substance from DOE's transparently inadequate plans for the future of Hanford. Yet everything now depends on those who are ready to act on their vision, and to contribute to the struggle of tribal and state representatives who do not accept the currently inadequate federal dictates. Art, science, activism, education, the courts, the voting booths and the media are all avenues for the beginning of a systemic change, which must ultimately be embodied in our concrete economic life and in our day-to-day behaviors as users of vulnerable and damaged lands.</p>

        <p>Hanford, in particular, is both a real-life ecological disaster and a potent symbol of the responsibility that we have when it comes to dealing with the legacies of twentieth-century modernism. The name of one of the key groups now working with the problem is signficant: that group is called The Hanford Challenge. The capacity to stick with such a challenge, despite all its difficulties, is one of the most important things that can be learned from ecological initiatives in Cascadia. At stake is a reinterpretation and fresh use of the human energies that created the problem in the first place. If three nuclear reactors and an entirely new process for the extraction of plutonium from spent fuel could be developed in the lapse of only two years, then why can't we now find a way to face and undo the mistakes that were part and parcel of past successes?</p>

        <p>The answer that we give to this question today and over the upcoming decades will be remembered and lived every day by the seventh generation.</p>

