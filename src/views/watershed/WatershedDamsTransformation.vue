        <p>Anyone who longs for wild rivers is going to be disappointed by the Columbia.</p>

        <p>The construction of municipal dams for hydropower and flood control on the Columbia and Snake rivers began as early as 1904. The far larger Bonneville and Grand Coulee dams were federal projects of the Roosevelt era, carried out by the Army Corps of Engineers and the Bueau of Reclamation, and completed in 1937 and 1941 respectively.</p>

        <p>The hopes of the time were high. Bonneville would facilitate navigation and provide a bounty of hydroelectric power for economic development, while irrigation from the Grand Coulee would make the desert bloom. A federal agency, the Bonneville Power Administration, would distribute the abundant electricity at low rates to all askers, via a system of high-tension lines and substations stretching across the Columbia Basin. And a new institution, the fish hatchery, would mitigate whatever damage might be done to the natural world. All of this would provide jobs and prospects for thousands of people left stranded by the Great Depression. In short, modernization and social progress would finally come to the Pacific Northwest.</p>

        <img src="images/Roosevelt.png" class="fullwidth">

        <p>Yet the war had other plans. The electricity from Bonneville powered massive bauxite reduction plants for the aluminum needs of the aviation industry; while that of Grand Coulee flowed directly into plutonium manufacturing at the Hanford Nuclear Reservation. What emerged from the Roosevelt era was not egalitarian development for the people, but instead, Cold War imperial America.</p>

        <p>On Memorial Day, 1948, floods were allowed to entirely destroy the workers' metropolis of Vanport, located on the riverbank between Vancouver and Portland. The flooding of Vanport became the justification for the Columbia River Treaty with Canada. The aim was to turn the entire basin into a single hydroelectric facility, featuring extensive storage reservoirs in the Canadian headwaters and multiple generating facilities down the mainstem, as well as further impoundments on the Snake River and other tributaries.</p>

        <p>As treaty negotiations dragged on, a veritable frenzy of dam-building seized the Pacific Northwest, with the future prospects of aluminum production, aviation, river commerce and recreation all dependent on increased hydrological control. Finally in 1964 the details of construction and compensation were haggled out, along with contracts for the sale of surplus Canadian hydroelectricity. The Columbia River Treaty was signed and the Mica, Duncan, Libby and Keenleyside dams were erected, creating an upriver storage system that canceled the chaotic surge of spring snowmelt.</p>

        <img src="images/BPA-Dittmer-Control-Center.jpg" class="fullwidth"></a>
        <div class="caption">BPA Dittmer Control Center, Vancouver, WA (source <a href="https://www.energy.gov/oe/articles/bpa-grid-modernization-and-setting-nation-success" target="_blank" rel="nofollow">here</a>)</div>

        <p>At present the Pacific Northwest Coordination Agreement regulates the annual flow of the entire transborder system, in a highly computerized attempt at harmonizing the fluctuating demands of power production, flood control, navigation, recreation and the survival of endangered fish species. Within a single river reach -- such as the Lower, Middle or Upper Columbia -- as many as ten dams will synchronize their water releases on an hourly basis, producing a managed diurnal pulse that has become second nature. Not the vagaries of the snowpack or the afternoon weather, but a logic of efficiency and optimization shapes the flow of the Columbia River.</p>

        <p>The construction of the water-management system signified the entry into a new era, not yet called the Anthropocene. By the close of the 1970s, when the frenzy of dam-building passed its peak and the Cold War weapons boom began subsiding, all human and non-human inhabitants of the basin were faced with the new reality of the river, which the environmental historian Richard White characterized as an "organic machine." Within this framework, White explained, labor has not "conquered" nature. Instead the transformative labor of technological production "involves human beings with the world so thoroughly that they can never be disentangled."</p>

        <a href="https://books.google.com/books?id=oIxpMxzG794C&printsec=frontcover#v=onepage&q&f=false" target="_blank" rel="nofollow"><img src="images/Organic_Machine.jpg" class="fullwidth"></a>

        <p>Still there was at least one problem with this new union of humanity and nature. The vast system of dams was slowly wiping out the emblematic species of the Pacific Northwest, the salmon, whose numbers began precipitously declining even as Native American bands, tribes and nations began an historic effort to recover their ancestral rights by catching fish "in all the usual and accustomed places." As public support for indigenous treaty claims expanded along with environmentalism, the "organic machine" that had literally forged the region's prosperity began to look like an alien power that had locked itself upon the meanders of the river: a dead hand of cement choking off the lifestream of Cascadia.</p>

        <p>In 1999, Ecotrust in Portland came out with the notion of <a href="https://ecotrust.org/publication/salmon-nation" target="_blank" rel="nofollow">Salmon Nation</a>. An entirely new goal had swum into view, which has rarely been explicitly stated but nonetheless has become quite clear. By helping to develop original forms of scientific expertise both within mainstream civil society and among the tribes, an expanded environmental movement could gain fresh sources of agency within the legal and administrative arenas opened up by the Endangered Species Act. The latter had the force of law, transforming citizens' convictions and scientists' biological opinions into instruments of tangible change. Many of those changes have already come into effect. What has emerged over the last two decades, within and against the rigid machinery of the dams, are the lineaments of a new kind of governance&mdash;the upturned foundations of a future bioregional state.</p>

        <a href="https://ecotrust.org/publication/salmon-nation-field-guide/" target="_blank" rel="nofollow"><img src="images/SalmonNationGuide.png" class="fullwidth"></a>

        <p>Today, the preservation of Pacific salmon, the expansion of Native American treaty rights, the resistance to fossil-fuel infrastructure and the recovery of riverine and forest ecosystems have become strong political demands, with clearly identified constituencies and forms of political representation stretching across Cascadia. The result is a palpable difficulty in maintaining the old system. As the Columbia River Treaty is renegotiated, it's clear that the hydrostate isn't working anymore.</p>

        <p>With the ongoing decline in the price of electricity&mdash;due particularly to the rise of solar in California&mdash;other facts about the dams have become apparent. Among them are the huge scars left on the landscape by constantly fluctuating water levels, even after the violence of forced population removal has faded. "These are industrial reservoirs. They are not lakes,” says Deb Kozak, the mayor of the city of Nelson in British Columbia. "These were advertised as lakes with great recreational opportunities when the dams were created. They are not."</p>

       <p> D.R. Michel, executive director of the Upper Columbia United Tribes, is even more forceful about the consequences of damming the river system: "It’s not just a machine to spin turbines and protect Portland. They permanently took the floods that happened annually down river and moved them up into our area, so we feel those impacts and those losses every day."</p>

       <p>Within the organic machine of the Columbia River, entanglement is the common condition. Salmon have emerged as totemic guides to a new kind of nation. Yet like the fish, we have to deal with what already exists. The real labor today is not returning to a wild state, but instead, finding a viable pulse for the Anthropocene metabolism.</p>

